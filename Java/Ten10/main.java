class main{
    public static void main(String[] args) {
        for(int i=10; i>1; i--){
            System.out.println("The value of i is: " + i);
    }
    System.out.println("This is the end of The I loop");
    int j=0;
    while(j<10){
        System.out.println("The value of j is: " + j);
        j++;
    }
    System.out.println("This is the start of The J loop");
  }   
}